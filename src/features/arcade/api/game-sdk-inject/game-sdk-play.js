/**
 * High-level play session for cartridges — boot, submit, persona.
 * Static-first tasks; dynamic AI uses author facts then lesson context.
 */
import { iframeSdkBlock } from './fragment.js';

export const GAME_SDK_PLAY_FRAGMENT = iframeSdkBlock([
    '',
    '    var playSessionConfig = {',
    '        persona: \'\',',
    '        modes: null,',
    '        maxTasks: 10,',
    '        includeCodeReplays: true,',
    '        maxAttempts: 24',
    '    };',
    '    var playSessionState = null;',
    '',
    '    function mergePlayOpts(opts) {',
    '        opts = opts || {};',
    '        return {',
    '            lang: opts.lang || user.lang || \'EN\',',
    '            max: opts.max != null ? opts.max : (opts.maxTasks != null ? opts.maxTasks : playSessionConfig.maxTasks),',
    '            maxTasks: opts.maxTasks != null ? opts.maxTasks : playSessionConfig.maxTasks,',
    '            modes: opts.modes || playSessionConfig.modes,',
    '            includeCodeReplays: opts.includeCodeReplays !== undefined ? opts.includeCodeReplays : playSessionConfig.includeCodeReplays,',
    '            maxAttempts: opts.maxAttempts != null ? opts.maxAttempts : playSessionConfig.maxAttempts',
    '        };',
    '    }',
    '',
    '    function filterTasksByModes(tasks, modes) {',
    '        if (!modes || !modes.length) return tasks;',
    '        var allow = Object.create(null);',
    '        for (var i = 0; i < modes.length; i++) allow[modes[i]] = true;',
    '        return (tasks || []).filter(function(t) { return allow[t.mode] || t.kind === \'code\'; });',
    '    }',
    '',
    '    function playSnapshot() {',
    '        var s = playSessionState;',
    '        if (!s) return null;',
    '        return {',
    '            lesson: s.lesson,',
    '            tasks: s.tasks.slice(),',
    '            taskIndex: s.taskIndex,',
    '            explore: !!s.explore,',
    '            codeReplays: (s.codeReplays || []).slice(),',
    '            currentTask: s.tasks[s.taskIndex] || null,',
    '            done: s.tasks.length > 0 && s.taskIndex >= s.tasks.length',
    '        };',
    '    }',
    '',
    '    function applyPlayHit(hit, opts, isStatic) {',
    '        if (!hit) return null;',
    '        var tasks = filterTasksByModes(hit.tasks, opts.modes);',
    '        playSessionState = {',
    '            lesson: hit.lesson,',
    '            tasks: tasks,',
    '            taskIndex: 0,',
    '            codeReplays: codeReplaysFromLesson(hit.lesson),',
    '            explore: !tasks.length && !isStatic',
    '        };',
    '        return playSnapshot();',
    '    }',
    '',
    '    async function playBoot(opts) {',
    '        opts = mergePlayOpts(opts);',
    '        var isStatic = aiMode === \'static\';',
    '        var first = null;',
    '        try { first = await bridge.getNextLesson(); } catch (e) { first = null; }',
    '        if (!first) return null;',
    '        var hit = await findLessonWithTasks(first, Object.assign({}, opts, { requireTasks: true }));',
    '        if (hit) return applyPlayHit(hit, opts, isStatic);',
    '        if (isStatic) return null;',
    '        playSessionState = {',
    '            lesson: first,',
    '            tasks: filterTasksByModes(buildTasksFromLesson(first, opts), opts.modes),',
    '            taskIndex: 0,',
    '            codeReplays: codeReplaysFromLesson(first),',
    '            explore: true',
    '        };',
    '        return playSnapshot();',
    '    }',
    '',
    '    async function playNextLesson(opts) {',
    '        opts = mergePlayOpts(opts);',
    '        var isStatic = aiMode === \'static\';',
    '        var hit = await findLessonWithTasks(null, Object.assign({}, opts, { requireTasks: isStatic }));',
    '        if (!hit) {',
    '            playSessionState = null;',
    '            return null;',
    '        }',
    '        return applyPlayHit(hit, opts, isStatic);',
    '    }',
    '',
    '    function playStartLesson(lesson, opts) {',
    '        if (!lesson) return null;',
    '        opts = mergePlayOpts(opts);',
    '        var isStatic = aiMode === \'static\';',
    '        var tasks = filterTasksByModes(buildTasksFromLesson(lesson, opts), opts.modes);',
    '        playSessionState = {',
    '            lesson: lesson,',
    '            tasks: tasks,',
    '            taskIndex: 0,',
    '            codeReplays: codeReplaysFromLesson(lesson),',
    '            explore: !tasks.length && !isStatic',
    '        };',
    '        return playSnapshot();',
    '    }',
    '',
    '    function playCurrentTask() {',
    '        if (!playSessionState) return null;',
    '        return playSessionState.tasks[playSessionState.taskIndex] || null;',
    '    }',
    '',
    '    async function playSubmit(input) {',
    '        if (!playSessionState) {',
    '            return { kind: \'unknown\', output: \'\', correct: false, missionAdvanced: false, done: false };',
    '        }',
    '        var s = playSessionState;',
    '        var task = s.tasks[s.taskIndex] || null;',
    '        var raw = String(input || \'\').trim();',
    '        if (!raw) {',
    '            return { kind: \'unknown\', output: \'\', correct: false, missionAdvanced: false, done: false };',
    '        }',
    '',
    '        if (task) {',
    '            var matched = matchesAnyAnswer(raw, task.accept);',
    '            if (matched.ok) {',
    '                s.taskIndex += 1;',
    '                return {',
    '                    kind: \'correct\',',
    '                    output: task.output || matched.matched || \'\',',
    '                    correct: true,',
    '                    missionAdvanced: true,',
    '                    done: s.taskIndex >= s.tasks.length,',
    '                    matched: matched.matched',
    '                };',
    '            }',
    '            var near = false;',
    '            for (var ni = 0; ni < (task.accept || []).length; ni++) {',
    '                var a = raw.toLowerCase().trim();',
    '                var b = String(task.accept[ni]).toLowerCase().trim();',
    '                if (a.length > 2 && b.length > a.length && b.indexOf(a) >= 0) { near = true; break; }',
    '            }',
    '            if (near && aiMode === \'static\') {',
    '                return {',
    '                    kind: \'close\',',
    '                    output: task.accept[0] || \'\',',
    '                    correct: false,',
    '                    missionAdvanced: false,',
    '                    done: false',
    '                };',
    '            }',
    '        }',
    '',
    '        var replay = findCodeReplayMatch(raw, s.codeReplays);',
    '        if (replay) {',
    '            var replayAdvanced = false;',
    '            if (task && task.kind === \'code\' && matchesAnyAnswer(raw, task.accept).ok) {',
    '                s.taskIndex += 1;',
    '                replayAdvanced = true;',
    '            }',
    '            return {',
    '                kind: \'replay\',',
    '                output: replay.replay.output,',
    '                correct: replayAdvanced,',
    '                missionAdvanced: replayAdvanced,',
    '                done: s.tasks.length > 0 && s.taskIndex >= s.tasks.length,',
    '                fuzzy: replay.fuzzy,',
    '                replayCmd: replay.replay.cmd',
    '            };',
    '        }',
    '',
    '        if (aiMode === \'dynamic\') {',
    '            try {',
    '                var aiRes = await askJSONImpl(lessonActionPrompt(s.lesson, raw, { persona: playSessionConfig.persona }), null, {});',
    '                var out = String(aiRes && aiRes.output || \'\').trim();',
    '                var advanced = false;',
    '                if (task && matchesAnyAnswer(raw, task.accept).ok) {',
    '                    s.taskIndex += 1;',
    '                    advanced = true;',
    '                }',
    '                return {',
    '                    kind: \'ai\',',
    '                    output: out,',
    '                    correct: advanced,',
    '                    missionAdvanced: advanced,',
    '                    done: s.tasks.length > 0 && s.taskIndex >= s.tasks.length,',
    '                    matchesLesson: aiRes && aiRes.matches_lesson !== false',
    '                };',
    '            } catch (e) {',
    '                return {',
    '                    kind: \'ai_error\',',
    '                    output: \'\',',
    '                    correct: false,',
    '                    missionAdvanced: false,',
    '                    done: false,',
    '                    error: e',
    '                };',
    '            }',
    '        }',
    '',
    '        return { kind: \'unknown\', output: \'\', correct: false, missionAdvanced: false, done: false };',
    '    }',
    '',
    '    function playReset(opts) {',
    '        if (!playSessionState) return null;',
    '        playSessionState.taskIndex = 0;',
    '        if (opts && opts.reload && playSessionState.lesson) {',
    '            var merged = mergePlayOpts(opts);',
    '            playSessionState.tasks = filterTasksByModes(buildTasksFromLesson(playSessionState.lesson, merged), merged.modes);',
    '            playSessionState.explore = !playSessionState.tasks.length && aiMode !== \'static\';',
    '        }',
    '        return playSnapshot();',
    '    }',
    '',
    '    function playConfigure(opts) {',
    '        opts = opts || {};',
    '        if (opts.persona != null) playSessionConfig.persona = String(opts.persona);',
    '        if (opts.modes != null) playSessionConfig.modes = opts.modes;',
    '        if (opts.maxTasks != null) playSessionConfig.maxTasks = Number(opts.maxTasks) || 10;',
    '        if (opts.includeCodeReplays != null) playSessionConfig.includeCodeReplays = !!opts.includeCodeReplays;',
    '        if (opts.maxAttempts != null) playSessionConfig.maxAttempts = Number(opts.maxAttempts) || 24;',
    '        return Object.assign({}, playSessionConfig);',
    '    }',
    '',
]);
